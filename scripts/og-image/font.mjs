/**
 * Draw a string in pixel font onto a canvas context.
 * @param {CanvasRenderingContext2D} ctx
 * @param {string} text
 * @param {number} x - top-left x
 * @param {number} y - top-left y
 * @param {number} pixelSize - size of each "pixel" in canvas pixels
 * @param {string} color - fill color
 */
export function drawPixelText(ctx, text, x, y, pixelSize = 3, color = '#e4e4e7') {
  ctx.fillStyle = color
  const chars = text.toUpperCase().split('')
  let cursorX = x

  for (const char of chars) {
    const glyph = GLYPHS[char]
    if (!glyph) {
      cursorX += pixelSize * 4 // unknown char = space
      continue
    }

    for (let row = 0; row < 7; row++) {
      for (let col = 0; col < 5; col++) {
        if (glyph[row] & (1 << (4 - col))) {
          ctx.fillRect(
            cursorX + col * pixelSize,
            y + row * pixelSize,
            pixelSize,
            pixelSize
          )
        }
      }
    }
    cursorX += pixelSize * 6 // 5 wide + 1 spacing
  }

  return cursorX - x // return total width drawn
}

/**
 * Measure the width of a pixel text string.
 */
export function measurePixelText(text, pixelSize = 3) {
  return text.length * pixelSize * 6
}
